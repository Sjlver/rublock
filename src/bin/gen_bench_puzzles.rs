// Generates a set of benchmark puzzles for benches/solver_bench.rs.
//
// Repeatedly runs a randomised DFS from an empty grid. Each attempt either
// reaches a complete grid (one candidate per cell, tried in a fresh random
// order) or hits a dead end and is abandoned. Valid unique-solution grids are
// collected into two buckets by difficulty:
//   - "easy":  QueueSolverState::new() alone solves the puzzle (no backtracking)
//   - "hard":  backtracking is required
//
// Usage:
//   cargo run --bin gen_bench_puzzles
//
// Paste the printed PUZZLES array into benches/solver_bench.rs.

use rand::seq::SliceRandom;
use rublock::grid::{Cell, Grid};
use rublock::queue_solver::QueueSolverState;
use rublock::solver::Puzzle;

const N: usize = 6;
const DIGITS: u8 = (N - 2) as u8; // 4 for a 6×6 grid
const TARGET_PER_BUCKET: usize = 5;

fn main() {
    let mut rng = rand::rng();
    let mut easy: Vec<([u8; N], [u8; N])> = Vec::new();
    let mut hard: Vec<([u8; N], [u8; N])> = Vec::new();

    while easy.len() < TARGET_PER_BUCKET || hard.len() < TARGET_PER_BUCKET {
        let mut cells = [[Cell::Empty; N]; N];
        if let Some(grid) = dfs(&mut cells, 0, &mut rng) {
            let (row_targets, col_targets) = grid.compute_targets();
            let puzzle = Puzzle::new(row_targets, col_targets);
            let state = QueueSolverState::new(puzzle);
            if state.count_solutions(2) == 1 {
                if state.is_solved() {
                    if easy.len() < TARGET_PER_BUCKET {
                        easy.push((row_targets, col_targets));
                    }
                } else if hard.len() < TARGET_PER_BUCKET {
                    hard.push((row_targets, col_targets));
                }
            }
        }
    }

    println!("// Generated by `cargo run --bin gen_bench_puzzles`");
    println!("const PUZZLES: &[([u8; 6], [u8; 6])] = &[");
    for (row_targets, col_targets) in &easy {
        println!("    // propagation-only");
        println!("    ({row_targets:?}, {col_targets:?}),");
    }
    for (row_targets, col_targets) in &hard {
        println!("    // needs backtracking");
        println!("    ({row_targets:?}, {col_targets:?}),");
    }
    println!("];");
    println!("// {} easy + {} hard puzzles", easy.len(), hard.len());
}

// Attempt to fill `cells` from `pos` onward, trying candidates in a random
// order at each position. Returns `Some(Grid)` if a complete grid was reached,
// or `None` if every candidate at some position was exhausted (dead end).
fn dfs(cells: &mut [[Cell; N]; N], pos: usize, rng: &mut impl rand::Rng) -> Option<Grid<N>> {
    if pos == N * N {
        return Some(Grid { cells: *cells });
    }

    let row = pos / N;
    let col = pos % N;

    let row_blacks = (0..col).filter(|&c| cells[row][c] == Cell::Black).count();
    let col_blacks = (0..row).filter(|&r| cells[r][col] == Cell::Black).count();
    let row_digit_mask: u64 = (0..col)
        .filter_map(|c| {
            if let Cell::Number(n) = cells[row][c] {
                Some(1u64 << n)
            } else {
                None
            }
        })
        .fold(0, |a, b| a | b);
    let col_digit_mask: u64 = (0..row)
        .filter_map(|r| {
            if let Cell::Number(n) = cells[r][col] {
                Some(1u64 << n)
            } else {
                None
            }
        })
        .fold(0, |a, b| a | b);

    let mut candidates: Vec<Cell> = std::iter::once(Cell::Black)
        .chain((1..=DIGITS).map(Cell::Number))
        .filter(|&c| match c {
            Cell::Black => row_blacks < 2 && col_blacks < 2,
            Cell::Number(d) => {
                let bit = 1u64 << d;
                row_digit_mask & bit == 0 && col_digit_mask & bit == 0
            }
            Cell::Empty => unreachable!(),
        })
        .collect();

    candidates.shuffle(rng);

    for candidate in candidates {
        cells[row][col] = candidate;
        if let Some(grid) = dfs(cells, pos + 1, rng) {
            return Some(grid);
        }
    }

    cells[row][col] = Cell::Empty;
    None
}
